public class Quiz6 {

	public static void main(String[] args) {
		
		//short형 변수 s를 선언하고 값 10을 대입하세요
		short s = 10;
		
		//int형 변수 i를 선언하고 값 20을 대입하세요
		int i = 20;
		
		//short는 2바이트, int는 4바이트 이 값을 합칠때 short보다 큰 타입에 담아줘야함
		//정수형 변수 sum을 선언하고 두 수의 합을 저장하세요
		int sum = s + i;
		
		System.out.println(sum);
		
	}

}
